export function getFeaturedMushrooms(): MushroomSpecies[] {
  return mushroomDatabase.filter((m) => m.featured)
}

export function getMushroomByScientificName(name: string): MushroomSpecies | undefined {
  return mushroomDatabase.find((m) => m.scientificName === name)
}

export function searchMushrooms(query: string): MushroomSpecies[] {
  const q = query.toLowerCase().trim()
  if (!q) return mushroomDatabase
  return mushroomDatabase.filter(
    (m) =>
      m.scientificName.toLowerCase().includes(q) ||
      m.commonNames.some((n) => n.toLowerCase().includes(q)) ||
      m.description.toLowerCase().includes(q) ||
      m.family.toLowerCase().includes(q),
  )
}

export function filterByCategory(species: MushroomSpecies[], category: string): MushroomSpecies[] {
  if (!category || category === 'todas') return species
  // Handle edibility-level "categories" (excelente, mortifero, etc.)
  const edibilityLevels: string[] = [
    'excelente',
    'buen_comestible',
    'comestible_con_cautela',
    'toxico',
    'mortifero',
    'no_recomendado',
  ]
  if (edibilityLevels.includes(category)) {
    return species.filter((m) => m.edibility === category)
  }
  return species.filter((m) => m.categories.includes(category))
}

export function filterByEdibility(
  species: MushroomSpecies[],
  edibility: EdibilityLevel | 'todas',
): MushroomSpecies[] {
  if (!edibility || edibility === 'todas') return species
  return species.filter((m) => m.edibility === edibility)
}

