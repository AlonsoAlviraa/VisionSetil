export type EdibilityLevel =
  | 'excelente'
  | 'buen_comestible'
  | 'comestible_con_cautela'
  | 'no_recomendado'
  | 'toxico'
  | 'mortifero'
  | 'desconocido'

export interface MushroomSpecies {
  /** Scientific name (used as unique key and for Wikipedia lookup) */
  scientificName: string
  /** Common Spanish name(s) */
  commonNames: string[]
  /** Short tagline shown on cards */
  tagline: string
  /** Longer description paragraph */
  description: string
  /** Taxonomic family */
  family: string
  /** Typical habitat */
  habitat: string
  /** Fruiting season in Spain */
  season: string
  /** Cap (sombrero) description */
  cap: string
  /** Stalk (pie) description */
  stem: string
  /** Gills/pores (himenio) description */
  hymenium: string
  /** Edibility classification */
  edibility: EdibilityLevel
  /** If toxic, what are the symptoms / toxin */
  toxicity?: string
  /** Key identification features */
  keyFeatures: string[]
  /** Dangerous look-alikes */
  lookAlikes?: string[]
  /** Categories for filtering */
  categories: string[]
  /** Emoji icon for quick visual */
  icon: string
  /** Whether this species is featured on the home page */
  featured?: boolean
}

